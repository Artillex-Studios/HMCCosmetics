package com.hibiscusmc.hmccosmetics.util;

public class TranslationPair {

    private String key;
    private String value;

    public TranslationPair(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
